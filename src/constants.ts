// IMPLEMENTATION: Different Leaders
export const getLeaderImage = (name: string) => {
    const lowerName = name.toLowerCase();
    
    // Match "BOLA AHMED TINUBU GCFR"
    if (lowerName.includes("tinubu")) {
        return "/images/bola.webp";
    }
    
    // Match "COMR. AYODELE OLAWANDE"
    if (lowerName.includes("ayodele")) {
        return "/images/ayodele.webp";
    }

    // Match "MR OLUBUNMI OLUSANYA" (Permanent Secretary)
    if (lowerName.includes("olusanya")) {
        return "/images/ps.webp";
    }
    
    // Fallback if no name matches
    const seed = name.replace(/[^a-zA-Z0-9]/g, '').toLowerCase();
    return `https://picsum.photos/300/300?random=leader-${seed}`;
}

